/*********************************************************************
*
*       _cbBkWindow
*/
static void _cbBkWindow(WM_MESSAGE* pMsg) {
  switch (pMsg->MsgId) {
  case WM_PAINT:
    _OnPaintBkWin();
    break;
  case WM_NOTIFY_PARENT:
    if (pMsg->Data.v == WM_NOTIFICATION_RELEASED) {
      int Id = WM_GetId(pMsg->hWinSrc);
      if (Id == ID_BUTTON_START) {
        _NextStep(0);
      }
    }
    break;
  case WM_KEY:
  case WM_TOUCH:
  case WM_PID_STATE_CHANGED:
#if GUI_SUPPORT_MOUSE
  case WM_MOUSEOVER:
#endif
    _ScreenSaverOff();
    break;
  default:
    WM_DefaultProc(pMsg);
  }
}

/*********************************************************************
*
*       _cbScreenSaver
*/
static void _cbScreenSaver(WM_MESSAGE* pMsg) {
  switch (pMsg->MsgId) {
  case WM_PAINT:
    GUI_SetBkColor(GUI_BLACK);
    GUI_Clear();
    _DrawMicriumLogo();
    GUI_DrawBitmap(&_LogoNEC, _NECLogoX, _NECLogoY);
    break;
  case WM_KEY:
  case WM_TOUCH:
  case WM_PID_STATE_CHANGED:
#if GUI_SUPPORT_MOUSE
  case WM_MOUSEOVER:
#endif
    _ScreenSaverOff();
    break;
  default:
    WM_DefaultProc(pMsg);
  }
}

