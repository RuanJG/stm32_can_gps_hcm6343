/*********************************************************************
*
*       _HandleCompanyLogo
*/
static void _HandleCompanyLogo(void) {
  if (_hWinScreenSaver) {
    if ((GUI_GetTime() - _NECLogoPrevTime) >= 50) {
      _NECLogoPrevTime = GUI_GetTime();
      _InvalidateRect(_hWinScreenSaver, _NECLogoX, _NECLogoY, _LogoNEC.XSize, _LogoNEC.YSize);
      _NECLogoX += _NECLogoDX;
      if (_NECLogoX < 0) {
        _NECLogoX  = 0;
        _NECLogoDX = -_NECLogoDX;
      } else if (_NECLogoX > (LCD_GetXSize() - _LogoNEC.XSize)) {
        _NECLogoX  = LCD_GetXSize() - _LogoNEC.XSize - 1;
        _NECLogoDX = -_NECLogoDX;
      }
      _NECLogoY += _NECLogoDY;
      if (_NECLogoY < 0) {
        _NECLogoY  = 0;
        _NECLogoDY = -_NECLogoDY;
      } else if (_NECLogoY > (LCD_GetYSize() - _LogoNEC.YSize)) {
        _NECLogoY  = LCD_GetYSize() - _LogoNEC.YSize - 1;
        _NECLogoDY = -_NECLogoDY;
      }
      _InvalidateRect(_hWinScreenSaver, _NECLogoX, _NECLogoY, _LogoNEC.XSize, _LogoNEC.YSize);
    }
  }
  if ((GUI_GetTime() - _MicriumLogoPrevTime) >= LOGO_WAITTIME) {
    _MicriumLogoDivisor  = 60000 / RPM / PI2;
    _MicriumLogoAngle   += (GUI_GetTime() - _MicriumLogoPrevTime) / _MicriumLogoDivisor;
    _MicriumLogoPrevTime = GUI_GetTime();
    _MicriumLogoMulX     = cos(_MicriumLogoAngle) * LOGOMULX;
    if (_MicriumLogoMulX != _MicriumLogoPrevMulX) {
      int x, y, w, h;
      _MicriumLogoPrevMulX = _MicriumLogoMulX;
      w = (_MicriumLogo.XSize * LOGOMULX / 1000) + 1;
      h = (_MicriumLogo.YSize * LOGOMULY / 1000) + 1;
      x = LCD_GetXSize() - w - 6;
      y = LCD_GetYSize() - h - 6;
      if (_hWinScreenSaver) {
        _InvalidateRect(_hWinScreenSaver, x, y, w, h);
      } else {
        _InvalidateRect(WM_HBKWIN, x, y, w, h);
      }
    }
  }
}

